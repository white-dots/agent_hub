def create_hub(
    project_root: str = ".",
    enable_parallel: bool = False,
) -> AgentHub:
    """Create an AgentHub configured for this project.

    Args:
        project_root: Path to the project root.
        enable_parallel: Enable parallel session execution.

    Returns:
        Configured AgentHub instance.
    """
    config = AutoAgentConfig(
        min_folder_size_kb=10,
        min_files_per_folder=2,
        max_agent_context_kb=80,
        max_depth=4,
        ignore_patterns={ignore_patterns},
    )

    hub, summary = discover_all_agents(project_root, config=config)
    print(summary)

    if enable_parallel:
        from agenthub.parallel import ParallelExecutionConfig
        hub.enable_parallel_sessions(
            project_root,
            config=ParallelExecutionConfig(max_parallel_sessions=3),
        )
        print("Parallel sessions enabled")

    return hub


