class TestGenerateRepoMap:
    def test_contains_header(self, map_graph):
        result = generate_repo_map(map_graph)
        assert "## Repo Map (auto-generated by agenthub)" in result

    def test_contains_module_count(self, map_graph):
        result = generate_repo_map(map_graph)
        # Should mention the total module count
        assert "modules" in result

    def test_contains_key_modules(self, map_graph):
        result = generate_repo_map(map_graph)
        # user.py should be identified as a key module (many dependents)
        assert "user.py" in result

    def test_contains_directory_layout(self, map_graph):
        result = generate_repo_map(map_graph)
        assert "Directory layout" in result

    def test_contains_editing_guidance_for_hubs(self, map_graph):
        """If there are high-impact modules, editing guidance should appear."""
        result = generate_repo_map(map_graph)
        # The map should have at least the key modules section
        assert "Key modules" in result

    def test_respects_max_lines(self, map_graph):
        result = generate_repo_map(map_graph, max_lines=20)
        line_count = len(result.split("\n"))
        # Should be reasonably close to the limit (soft cap)
        assert line_count < 40  # Allow some overflow since it's a soft cap

    def test_shows_exports_for_hubs(self, map_graph):
        result = generate_repo_map(map_graph)
        # Hub modules should have their exports listed
        # user.py exports User, UserRole, to_dict etc.
        assert "User" in result


