class TestMCPResourceIntegration:
    def test_resources_list(self):
        from agenthub.mcp_server import RESOURCES

        assert len(RESOURCES) == 1
        assert RESOURCES[0]["uri"] == "repo://map"
        assert RESOURCES[0]["mimeType"] == "text/markdown"

    def test_get_repo_map_returns_content(self, map_graph, map_project):
        from agenthub.mcp_server import get_repo_map

        with patch("agenthub.mcp_server.get_graph", return_value=map_graph):
            result = get_repo_map()

        # Reset cache for other tests
        import agenthub.mcp_server as mcp
        mcp._repo_map_cache = None

        assert result is not None
        assert "Repo Map" in result
        assert "modules" in result

    def test_get_repo_map_caches(self, map_graph):
        from agenthub.mcp_server import get_repo_map
        import agenthub.mcp_server as mcp

        mcp._repo_map_cache = None

        with patch("agenthub.mcp_server.get_graph", return_value=map_graph):
            result1 = get_repo_map()
            # Second call should use cache (get_graph won't be called)
            with patch("agenthub.mcp_server.get_graph", return_value=None):
                result2 = get_repo_map()

        assert result1 == result2

        # Cleanup
        mcp._repo_map_cache = None

    def test_get_repo_map_no_graph(self):
        from agenthub.mcp_server import get_repo_map
        import agenthub.mcp_server as mcp

        mcp._repo_map_cache = None

        with patch("agenthub.mcp_server.get_graph", return_value=None):
            result = get_repo_map()

        assert result is None


class TestCLIMapCommand:
    def test_stdout_flag(self, map_project):
        """Test that --stdout prints to stdout instead of writing file."""
        import argparse

        from agenthub.cli import cmd_map

        args = argparse.Namespace(path=str(map_project), stdout=True, verbose=False)

        # Should succeed (return 0)
        result = cmd_map(args)
        assert result == 0

        # Should NOT create CLAUDE.md when using --stdout
        claude_md = map_project / "CLAUDE.md"
        assert not claude_md.exists()

    def test_creates_claude_md(self, map_project):
        """Test that running without --stdout creates CLAUDE.md."""
        import argparse

        from agenthub.cli import cmd_map

        args = argparse.Namespace(path=str(map_project), stdout=False, verbose=False)

        result = cmd_map(args)
        assert result == 0

        claude_md = map_project / "CLAUDE.md"
        assert claude_md.exists()
        content = claude_md.read_text()
        assert "Repo Map" in content
        assert "impact_check" in content

    def test_appends_to_existing_claude_md(self, map_project):
        """If CLAUDE.md exists, append the repo map."""
        import argparse

        from agenthub.cli import cmd_map

        # Create existing CLAUDE.md
        claude_md = map_project / "CLAUDE.md"
        claude_md.write_text("# My Project\n\nExisting content here.\n")

        args = argparse.Namespace(path=str(map_project), stdout=False, verbose=False)
        result = cmd_map(args)
        assert result == 0

        content = claude_md.read_text()
        assert "Existing content here." in content
        assert "Repo Map" in content

    def test_updates_existing_repo_map(self, map_project):
        """If CLAUDE.md already has a repo map, replace it."""
        import argparse

        from agenthub.cli import cmd_map

        # Create existing CLAUDE.md with old repo map
        claude_md = map_project / "CLAUDE.md"
        claude_md.write_text(
            "# My Project\n\n"
            "## Repo Map (auto-generated by agenthub)\n\n"
            "OLD CONTENT\n\n"
            "## Other Section\n\nKeep this.\n"
        )

        args = argparse.Namespace(path=str(map_project), stdout=False, verbose=False)
        result = cmd_map(args)
        assert result == 0

        content = claude_md.read_text()
        assert "OLD CONTENT" not in content
        assert "Other Section" in content
        assert "Keep this." in content
        assert "Repo Map" in content
